package gymlife.utility;
/**
 * The ScenariosType enum represents different types of scenarios in the gym life application.
 * It is used to specify the type of scenario for various game elements.
 */
public enum ScenariosType {
    /**
     * Represents the main map scenario.
    */
    MAIN_MAP,
    /**
    * Represents the gym scenario.
    */
    MINIGAME_GYM,
    /**
    * Represents the bank scenario.
    */
    MINGAME_BANK,
    /**
     * Represents the indoor maps' scenario.
     */
    INDOOR_MAP,
    /**
     * Represents the sleeping scenario.
     */
    SLEEPING;
}
